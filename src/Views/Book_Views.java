    private DefaultComboBoxModel<String> getNoKamar() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        
        try {
            String query = "SELECT no_kamar FROM kamar_tersedia WHERE status = 'available'";
            PreparedStatement statement = connection.prepareStatement(query);

            ResultSet resultSet = statement.executeQuery();
            while (resultSet.next()) {
                model.addElement(String.valueOf(resultSet.getInt("no_kamar")));
            }
            resultSet.close();
            statement.close();
//            connection.close();
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Load data gagal, cobalah beberapa saat lagi" + e.getMessage());
        }
        
        return model;
    }
    
    private String generateRandomTarif(String tarif){
        // Hapus 3 digit terakhir
        String modifiedString = tarif.substring(0, tarif.length() - 3);
        
        // Menghasilkan angka unik random dengan tiga digit
        Random random = new Random();
        int randomDigits = random.nextInt(999);
        
        // Menggabungkan angka unik random dengan string yang telah diubah
        String result = modifiedString + String.format("%03d", randomDigits);
        
        return result;
    }

