    private void jbSelesaiBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSelesaiBookActionPerformed
        // Sanity check
        if (jtfNIMBook.getText().equals("") || jtfNIMBook.getText().length() < 6){
            JOptionPane.showMessageDialog(null, "Harap isi NIM dan pastikan lebih dari 5 karakter");
        }
        try{
            int nimInt = Integer.parseInt(jtfNIMBook.getText());
            // Check dulu sudah ada di DB atau belum
            String query = "UPDATE kamar_tersedia SET booked_by = ? WHERE no_kamar = ?;";
            PreparedStatement statement = connection.prepareStatement(query);
            statement.setString(1, String.valueOf(userID)); // set nilai booked_by (pakai interpolasi)
            statement.setString(2, jComboBox1.getSelectedItem().toString()); // set nilai no_kamar (pakai interpolasi)

            int rowsUpdated = statement.executeUpdate();
    
            if (rowsUpdated > 0) {
                query = "SELECT tarif FROM kamar_tersedia WHERE no_kamar = ?";
                statement = connection.prepareStatement(query);
                statement.setString(1, jComboBox1.getSelectedItem().toString()); // set nilai no_kamar (pakai interpolasi)
                ResultSet resultSet = statement.executeQuery();
                if (resultSet.next()) {
                    String newTarif = generateRandomTarif(resultSet.getString("tarif"));
                    JOptionPane.showMessageDialog(null, "Registrasi berhasil, transfer ke rekening dengan nominal berikut: "+newTarif);
                    mainMenuViews.setVisible(true);
                    dispose();
                } else {}
            } else {
                JOptionPane.showMessageDialog(null, "Terdapat kesalahan pada sistem, coba lagi nanti.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Harap masukkan format angka di NIM"+e.getMessage());
        }
    }//GEN-LAST:event_jbSelesaiBookActionPerformed

